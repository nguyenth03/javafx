public class Accountdb {
       private String id,UID,username,password,full_name,gender,email,dob,mobile,status,roleId;

    public Accountdb(String id, String username) {
        this.id = id;
        this.username = username;
    }


    public Accountdb(String id, String UID, String username, String password, String full_name, String gender, String email, String dob, String mobile, String status, String roleId) {
        this.id = id;
        this.UID = UID;
        this.username = username;
        this.password = password;
        this.full_name = full_name;
        this.gender = gender;
        this.email = email;
        this.dob = dob;
        this.mobile = mobile;
        this.status = status;
        this.roleId = roleId;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUID() {
        return UID;
    }

    public void setUID(String UID) {
        this.UID = UID;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getFull_name() {
        return full_name;
    }

    public void setFull_name(String full_name) {
        this.full_name = full_name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getRoleId() {
        return roleId;
    }

    public void setRoleId(String roleId) {
        this.roleId = roleId;
    }

    @Override
    public String toString() {
        return  username ;
    }

}
